#Indexing- to extract a single element/character in a string
x='ABCDEF'
print(x[2])
print('*********End of pgm1*******')
print(x[-1])
print('*********End of pgm1*******')


print('**********Slicing the string************')
print(x[:3])
print(x[2:4])
print(x[::2])


print('**********Reversing the string************')
print(x[::-1])

